package com.syntax.class09;

public class Recap {

	public static void main(String[] args) {
		
		for(int i=1; i<=5; i++) {
			System.out.println("hello");
			
			for(int y=1; y<=3; y++) {
				System.out.println("bye");
			}
			
		}
		System.out.println("------------------------=");
		for(int i=1; i<=5; i--) {
			System.out.println("hello");
			
			for(int y=1; y<=3; y++) {
				System.out.println("bye");
			}
		

		}
	}
}
