package com.syntax.class08;

public class FoorLoopExamples {

	public static void main(String[] args) {
		
		for(int i=1; i<=90; i++) {
		       System.out.print(i+" ");
			}
		System.out.println("---------------------");
		// number 60 to 10 
		for(int i=60; i>=10; i-- ) {
			System.out.print(i+" ");
		}
		System.out.println("------------------------");
		//what is the output?
		
		for(int i=5; i<=40; i+=5 ) {
			System.out.print(i+" ");
		}
		System.out.println("----------------------");
        for(int i=1; i<=100; i++) {
        	System.out.print(i+" ");
        }
        System.out.println("-------------------");
        for(int i=100; i>=1; i--) {
        	System.out.print(i+" ");
        }
        System.out.println("-----------------");
        for(int i=20; i>=1; i-=2) {
        	System.out.print(i+" ");
        }
        System.out.println("----------------------");
        for(int i=21; i<=50; i+=2) {
        	System.out.print(i+" ");
        }
		

	}

}
